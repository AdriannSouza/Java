    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        //Define como visível assim que o botão for acionado.
        int a, b, c;
        a = sliA.getValue();
        b = sliB.getValue();
        c = sliC.getValue();
        if (a < b+c && b < a+c && c < a+b) {
            lblStatus.setText("Formam um triângulo");
            
            //Se os lados forem todos iguais
            if (a==b && b==c) {
                lblTipo.setText("Equilatero");
            
            //Se os lados forem todos diferentes
            } else if (a!=b && b!=c && a!=c) {
                lblTipo.setText("Escaleno");
            }
            
            //Se dois lados forem iguais, ou seja, se não forem todos iguais ou todos diferentes.
            else {
                lblTipo.setText("Isosceles");
            }
            
        } else {
            lblStatus.setText("NÃO Formam um triângulo");
            lblTipo.setText("---");
        }
        panResposta.setVisible(true);
    }//GEN-LAST:event_btnVerificarActionPerformed

